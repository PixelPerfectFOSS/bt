async function fontsList(options) {
    const fontsDir = unixify(path.resolve("./Fonts"));
    const pattern = `${fontsDir}/**/*.{ttf,otf}`;
    const files = await glob.promise(pattern);
    return {
        fonts: files.map((file) => path.basename(file))
    };
}

